    private void addCourseScheduleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCourseScheduleActionPerformed
        // TODO add your handling code here:
        String semester = currentSemesterLabel.getText();
        String CourseCode = (String)CourseComboBox.getSelectedItem();
        String[] fullName = ((String)StudentComboBoxCourse.getSelectedItem()).split(","); 
          
        int CourseSeats = CourseQueries.getCourseSeats(semester, CourseCode);
        int ScheduledStudentCount = ScheduleQueries.getScheduledStudentCount(semester, CourseCode);
        
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String StudentID = "";
        for (StudentEntry student: students){
            if ((student.getFirstName().equals(fullName[1])) && (student.getLastName().equals(fullName[0])))
                    {
                    StudentID = student.getStudentID();
                    break;
            }
            
    } 
        
        if ( ScheduledStudentCount < CourseSeats ){
            String Status = "S";
            ScheduleEntry newSchedule = new ScheduleEntry(semester, CourseCode, StudentID, Status);
            ScheduleQueries.addScheduleEntry(newSchedule);
            ScheduleCourseStatusLabel.setText(fullName[0] + " " + fullName[1] + " has been added.");
     
        }
        else{
            String Status = "W";
            ScheduleEntry newSchedule = new ScheduleEntry(semester, CourseCode, StudentID, Status);
            ScheduleQueries.addScheduleEntry(newSchedule);
            ScheduleCourseStatusLabel.setText(fullName[0] + " " + fullName[1] + " has been waitlisted");
            
        }        
        
    }//GEN-LAST:event_addCourseScheduleActionPerformed

    
