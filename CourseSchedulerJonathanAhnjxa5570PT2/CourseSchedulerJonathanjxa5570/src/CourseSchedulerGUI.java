    private void DropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DropStudentButtonActionPerformed
        // TODO add your handling code here:
        String StudentInfo = (String) DropStudentComboBox.getSelectedItem();
        String[] arrayofStudentInfo = StudentInfo.split(" ");
        String studentID = arrayofStudentInfo[1];
        StudentQueries.dropStudent(studentID);
        DropStudentTextArea.setText(StudentInfo + " has been dropped from the list of students.\n\n");
        
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        for (String semester: semesters){
            DropStudentTextArea.append("For Semester " + semester + "\n");
            ArrayList<ScheduleEntry> schedules = ScheduleQueries.getScheduleByStudent(semester, studentID);
            for (ScheduleEntry schedule: schedules){
                String courseCode = schedule.getCourseCode();
                ScheduleQueries.dropStudentScheduleByCourse(semester,studentID, courseCode);
                DropStudentTextArea.append(StudentInfo + " has been dropped from " + courseCode + "\n");
                
                ArrayList<ScheduleEntry> Schedules = ScheduleQueries.getWaitlistedStudentsByCourse(semester, courseCode);
                int CourseSeats = CourseQueries.getCourseSeats(semester, courseCode);
                int ScheduledStudentCount = ScheduleQueries.getScheduledStudentCount(semester, courseCode);
                                
                if ((!Schedules.isEmpty()) && (ScheduledStudentCount < CourseSeats)){
                    ScheduleEntry waitlistedSchedule = Schedules.get(0);
                    ScheduleEntry newSchedule = new ScheduleEntry(waitlistedSchedule.getSemester(), waitlistedSchedule.getCourseCode(), waitlistedSchedule.getStudentID(), "S");
                    ScheduleQueries.updateScheduleEntry(semester, newSchedule);
                    StudentEntry student = StudentQueries.getStudent(waitlistedSchedule.getStudentID());
                    String newStudentInfo = String.format("%s,%s %s", student.getLastName(), student.getFirstName(), student.getStudentID());
                    DropStudentTextArea.append(newStudentInfo + " has been scheduled into " + courseCode + "\n");
                
                }
            }
        }
        
        
        rebuildStudentComboBoxes();
        rebuilddropStudentComboBox();
        
        
    }//GEN-LAST:event_DropStudentButtonActionPerformed

