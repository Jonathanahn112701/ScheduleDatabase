        public void rebuildSemesterComboBoxes()
    {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.size() > 0)
        {
            currentSemesterLabel.setText(semesters.get(0));
        }
        else
        {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }
        
        public void rebuildStudentComboBoxes(){
            ArrayList<String> studentNames = new ArrayList<>();
            for (StudentEntry student: StudentQueries.getAllStudents()){
                studentNames.add(String.format("%s,%s", student.getLastName(), student.getFirstName()));
            }
            StudentComboBoxCourse.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));
            StudentComboBoxSchedule.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));
            
        }
            public void rebuilddropStudentComboBox(){
            ArrayList<String> studentNames = new ArrayList<>();
            for (StudentEntry student: StudentQueries.getAllStudents()){
                studentNames.add(String.format("%s,%s %s", student.getLastName(), student.getFirstName(), student.getStudentID()));
            }
            DropStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));
            StudentDropCourseStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));
            
        }
        
        public void rebuildCourseComboBoxes(){
            String semester = currentSemesterLabel.getText();
            ArrayList<String> courseCode = CourseQueries.getAllCourseCodes(semester);
            CourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courseCode.toArray()));
            DisplayCourseListComboBox.setModel(new javax.swing.DefaultComboBoxModel(courseCode.toArray()));
            DropCourseAdminComboBox.setModel(new javax.swing.DefaultComboBoxModel(courseCode.toArray()));
            StudentDropCourseCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courseCode.toArray()));

        }
        


